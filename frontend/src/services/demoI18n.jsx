export const useDemoLocale = (app) => {
  const { i18n } = useTranslation();
  const locale = i18n.language === "bn" ? "bn" : "en";
  const setLocale = (l) => i18n.changeLanguage(l);
  const t = STRINGS[app][locale];
  return { locale, setLocale, t };
};

export const LangToggle = ({ locale, setLocale, color = "white" }) => (
  <div data-testid="lang-toggle" className="inline-flex bg-white/10 backdrop-blur rounded-full border border-white/30 p-0.5 text-[10px] font-bold">
    {["bn", "en"].map((l) => (
      <button
        key={l}
        data-testid={`lang-${l}`}
        onClick={() => setLocale(l)}
        className={`px-2.5 py-1 rounded-full transition-all ${locale === l ? "bg-white text-slate-900" : `text-${color} opacity-70 hover:opacity-100`}`}
      >
        {l === "bn" ? "বাংলা" : "EN"}
      </button>
    ))}
  </div>
);

